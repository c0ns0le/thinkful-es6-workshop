export default function destructure() {
  console.log("Object Destructuring: ");

  /*
   * Declare an object as a constant
   * that contains a greeting, your first name, and last name, 
   * then use object destructuring to get the greeting and your last name.
   * Console.log your greeting, along with your choice of salutation, and your last name.
   * Bonus points for using string interpolation!
   */
   
   const obj = {greeting: 'hello' , firstName: 'harry', lastName: 'hedger' };
   const {greeting, lastName} = obj;
   console.log(`destructure greeting ${greeting} mre ${lastName}`);
}